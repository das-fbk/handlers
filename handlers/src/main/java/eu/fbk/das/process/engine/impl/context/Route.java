package eu.fbk.das.process.engine.impl.context;

import java.util.List;

public class Route {

    public String name;
    public List<PickupPoint> pickupPoints;

    public Route(String name, List<PickupPoint> pickupPoints) {
	this.name = name;
	this.pickupPoints = pickupPoints;
    }

    @Override
    public String toString() {
	return "[name=" + name + "]";
    }

}
